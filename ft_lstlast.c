#include "libft.h"

t_list	*ft_lstlast(t_list *lst)
{	
	t_list	*last;
	t_list	*begin;

	begin = lst;
	if (begin == 0)
		return (0);
	while (begin)
	{
		last = begin;
		begin = begin->next;
	}
	return (last);
}
