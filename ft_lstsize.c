#include "libft.h"

int	ft_lstsize(t_list *lst)
{
	int		nbr;
	t_list	*begin;

	nbr = 0;
	begin = lst;
	while (begin)
	{
		nbr++;
		begin = begin->next;
	}
	return (nbr);
}
